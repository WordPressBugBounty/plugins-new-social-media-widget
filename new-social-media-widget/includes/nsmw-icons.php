function nsmw_render_svg_icon($network, $size = '')
{
    if ($network === 'x-twitter') $network = 'x-twitter'; // normalize mapping if needed
    $icons = nsmw_get_svg_icons();
    if (isset($icons[$network])) {
        $icon = $icons[$network];

        $font_size_style = '';
        if ($size) {
            if ($size === 'lg') {
                $font_size_style = 'font-size: 1.33333em;';
            } elseif (is_numeric($size)) {
                $font_size_style = 'font-size: ' . esc_attr($size) . 'em;';
            }
        }

        return '<svg xmlns="http://www.w3.org/2000/svg" viewBox="' . esc_attr($icon['viewBox']) . '" class="nsmw-svg-icon ' . esc_attr($network) . '" style="width: 1.25em; height: 1em; fill: currentColor; vertical-align: -0.125em; ' . $font_size_style . '"><path d="' . esc_attr($icon['path']) . '"/></svg>';
    }
    return '';
}
